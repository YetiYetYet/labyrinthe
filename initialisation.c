// La fonction ajoute autant de gemes que le second argument a des positions vide du labyrinthes.
void ajouter_gemmes(Donnee* data, int nb) {
  int i;
  for(i = 0; i < nb; i++) {
    int x = rand() % LARGEUR;
    int y = rand() % HAUTEUR;
    if(data->tableau[y][x] == SOL) {
      data->tableau[y][x] = GEMME;
    } else {
      i--;
    }
  }
}